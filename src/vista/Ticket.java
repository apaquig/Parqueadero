    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        VehiculoEN v = vehiculoOn.buscarVehiculo(txtPlaca.getText());
        String horaEntrada = dateFormat.format(date);
        if (v.getVehiculoPlaca() != null) {
            ticketOn.registrar(txtNumero.getText(), horaEntrada, "", v);
            limpiarCajas();
            listar();
        } else {
            int opcion = JOptionPane.YES_NO_OPTION;
            int dialog = JOptionPane.showConfirmDialog(null, "No Existe Vehiculo desea agregar?", "Warning", opcion);
            if (dialog == 0) {
                veh = new Vehiculo();
                veh.setVisible(true);
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void tblListaTicketMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblListaTicketMouseClicked
        int fila = tblListaTicket.getSelectedRow();
        codigo = Integer.parseInt(tblListaTicket.getValueAt(fila, 0).toString());
        cargarDatos(codigo);
    }//GEN-LAST:event_tblListaTicketMouseClicked

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        VehiculoEN v = vehiculoOn.buscarVehiculo(txtPlaca.getText());
        String horaEntrada = dateFormat.format(date);
        String horaSalida = dateFormat.format(date);
        if (v.getVehiculoPlaca() != null) {
            ticketOn.modificar(codigo, txtNumero.getText(), horaEntrada,horaSalida, v);
            limpiarCajas();
            listar();
        } else {
            int opcion = JOptionPane.YES_NO_OPTION;
            int dialog = JOptionPane.showConfirmDialog(null, "No Existe Vehiculo desea agregar?", "Warning", opcion);
            if (dialog == 0) {
                veh = new Vehiculo();
                veh.setVisible(true);
            }
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

