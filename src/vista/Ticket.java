private void cargarDatos(int c) {
        TicketEN t = ticketOn.buscarTicket(c);
        txtNumero.setText(t.getNumeroTicket());
        txtPlaca.setText(t.getVeh().getVehiculoPlaca());
    }
private void limpiarCajas(){
txtNumero.setText("");
txtPlaca.setText("");
}
